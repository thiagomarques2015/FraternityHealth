package br.com.fraternityhealth.control;

import android.app.Activity;
import android.content.Context;
import android.support.annotation.NonNull;
import android.util.Log;
import android.widget.Toast;

import com.google.android.gms.tasks.OnCompleteListener;
import com.google.android.gms.tasks.Task;
import com.google.firebase.auth.AuthResult;
import com.google.firebase.auth.FirebaseAuth;

import br.com.fraternityhealth.model.FirebaseCommand;
import br.com.fraternityhealth.view.MainActivity;

/**
 * Created by Thiago on 16/03/2017.
 */

public class FirebaseLoginAnonymously implements FirebaseCommand {
    private static final String TAG = "LoginAnonymously";

    private Activity activity;

    public FirebaseLoginAnonymously(Activity activity) {
        this.activity = activity;
    }

    @Override
    public void execute(final Context context) {
        FirebaseAuth.getInstance().signInAnonymously()
                .addOnCompleteListener(activity, new OnCompleteListener<AuthResult>() {
                    @Override
                    public void onComplete(@NonNull Task<AuthResult> task) {
                        Log.d(TAG, "signInAnonymously:onComplete:" + task.isSuccessful());

                        // If sign in fails, display a message to the user. If sign in succeeds
                        // the auth state listener will be notified and logic to handle the
                        // signed in user can be handled in the listener.
                        if (!task.isSuccessful()) {
                            Log.w(TAG, "signInAnonymously", task.getException());
                            Toast.makeText(context, "Authentication failed.",
                                    Toast.LENGTH_SHORT).show();
                        }

                        // ...
                    }
                });
    }
}
